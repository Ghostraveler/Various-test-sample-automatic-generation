# coding:utf-8
import jieba.posseg as pseg


# 定义函数来评估句子的句法结构复杂度
def evaluate_syntax_complexity(sentence):
    # 分词并标注词性
    words = pseg.cut(sentence)

    # 初始化计数器
    noun_count = 0  # 名词数量
    verb_count = 0  # 动词数量
    adj_count = 0  # 形容词数量
    adv_count = 0  # 副词数量

    # 遍历词语并统计各类词性的数量
    for word, flag in words:
        if flag.startswith('n'):  # 名词
            noun_count += 1
        elif flag.startswith('v'):  # 动词
            verb_count += 1
        elif flag.startswith('a'):  # 形容词
            adj_count += 1
        elif flag.startswith('d'):  # 副词
            adv_count += 1

    # 计算句子的句法结构复杂度得分（简单计数，没有考虑具体的语法规则）
    syntax_complexity_score = noun_count + verb_count + adj_count + adv_count

    return syntax_complexity_score


